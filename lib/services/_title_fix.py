# 通用占位标题正则
PLACEHOLDER_RE = re.compile(
    r"title:\s*'(?:学习进阶指南|专家答疑解惑|实战案例分析|最新研究报告|常见误区避坑|5 分钟速通入门|热门播客精选|主播推荐合集|通勤必听榜单|深度访谈节目|轻松一刻段子|行业大咖专访)'"
)

PATH = "lib/services/news_service.dart"

with open(PATH, "r", encoding="utf-8") as f:
    src = f.read()

# 逐桶处理
fixed = 0
for bucket_key, new_titles in TITLES.items():
    # 找桶块
    pattern = re.compile(
        r"'" + re.escape(bucket_key) + r"':\s*\[(.*?)\n\s*\],",
        re.DOTALL,
    )
    m = pattern.search(src)
    if not m:
        print(f"SKIP {bucket_key}: not found")
        continue
    block = m.group(1)
    # 找 7-12 号（id 包含 _7 到 _12）
    # 每次匹配替换一个
    new_block = block
    title_iter = iter(new_titles)
    for n in range(7, 13):
        # 匹配 id 以 _n 结尾的 ContentItem
        item_re = re.compile(
            r"(ContentItem\(id:\s*'" + re.escape(bucket_key) + f"_{n}',\s*)title:\s*'[^']*'(,)",
        )
        replacement_titles = list(new_titles)
        # 简单起见: 对 7-12 顺序替换
        idx = n - 7
        if idx >= len(replacement_titles):
            continue
        new_title = replacement_titles[idx]
        new_block_new, count = item_re.subn(
            rf"\1title: '{new_title}'\2",
            new_block,
            count=1,
        )
        if count > 0:
            new_block = new_block_new
            fixed += count
        else:
            print(f"  {bucket_key}_{n}: no match")

    src = src[:m.start(1)] + new_block + src[m.end(1):]

with open(PATH, "w", encoding="utf-8") as f:
    f.write(src)

print(f"\u2705 Replaced {fixed} titles")
